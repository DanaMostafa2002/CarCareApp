package SDA.Models.Vehicle;

public class Dimensions {

    double length;
    double width;

    public Dimensions() {
        length = 0;
        width = 0;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

}
